    // Function to generate pagination links
    function generatePagination(currentPage, totalPages) {
        let paginationHtml = '';

        // Previous Page Link
        if (currentPage > 1) {
            paginationHtml += `<a href="?page=${currentPage - 1}" class="pagination__link">Prev</a>`;
        }
        
        currentPage >= 3 ? paginationHtml += `<a href="?page=1" class="pagination__link">1</a>` :'';
        currentPage >= 4 ? paginationHtml += `<b class="pagination__link">...</b>` :'';
        

        // Page Number Links
        for (let i = 1; i < totalPages; i++) {
            if (i >= currentPage - 1 && i <= currentPage + 1) {
                paginationHtml += `<a href="?page=${i}" class="pagination__link ${i === currentPage ? 'active' : ''}">${i}</a>`;
            }
            console.log('i===',i);
        }
        
        
        if(totalPages>3 ){
            //totalPages >= 3 && currentPage===(totalPages-3) ? paginationHtml += `<b class="pagination__link">...</b>` :'';
            totalPages >=3 && (currentPage>=1 && currentPage<=(totalPages-3) ) ? paginationHtml += `<b class="pagination__link">...</b>` :'';
            //totalPages=== 4 ? paginationHtml += `<b class="pagination__link">...</b>` :'';
            //totalPages>=3   ? paginationHtml += `<a href="?page=${totalPages}" class="pagination__link">${totalPages}</a>` :'';
        }
        
         paginationHtml += `<a href="?page=${totalPages}" class="pagination__link">${totalPages}</a>` ;
        
        // Next Page Link
        if (currentPage < totalPages) {
            paginationHtml += `<a href="?page=${currentPage + 1}" class="pagination__link">Next</a>`;
        }

        return paginationHtml;
    }

