const productsPerPage = 5;
let currentPage = 1;
const totalPages = Math.ceil(products.length / productsPerPage);

function renderProducts(page) {
const start = (page - 1) * productsPerPage;
const end = page * productsPerPage;
const currentProducts = products.slice(start, end);

const productContainer = document.getElementById('product-container');
productContainer.innerHTML = currentProducts.map(product => `
    
    <section style='border-bottom:1px solid #3D52A0;'>
        <div class="content">
        <p class="job-title">
            ${product.jobTitile}
        </p>
        <div class="details">
            <span>${product.updatedby}</span>
        </div>
        <p class="description">
            ${product.description}
        </p>
        <p class="read-more"><a href="${product.readMore}">Read More </a></p>
    </div>
    <div class="company-logo">
        <img src="${product.companyLogo}" alt="" srcset="">
    </div>
    </section>
    
`).join('');
}

function renderPagination() {
const pageNumbers = document.getElementById('page-numbers');
pageNumbers.innerHTML = '';

for (let i = 1; i <= totalPages; i++) {
    const pageLink = document.createElement('a');
    pageLink.href = '#';
    pageLink.textContent = i;
    pageLink.style.border='1px solid black';
    pageLink.style.padding='5px';
    pageLink.className = i === currentPage ? 'active' : '';
    pageLink.addEventListener('click', (e) => {
        e.preventDefault();
        currentPage = i;
        updateUI();
    });
    pageNumbers.appendChild(pageLink);
}

// document.getElementById('prev').classList.toggle('disabled', currentPage === 1);
// document.getElementById('next').classList.toggle('disabled', currentPage === totalPages);
}

function updateUI() {
renderProducts(currentPage);
renderPagination();
}

document.getElementById('prev').addEventListener('click', (e) => {
e.preventDefault();
if (currentPage > 1) {
    currentPage--;
    updateUI();
}
});

document.getElementById('next').addEventListener('click', (e) => {
e.preventDefault();
if (currentPage < totalPages) {
    currentPage++;
    updateUI();
}
});

